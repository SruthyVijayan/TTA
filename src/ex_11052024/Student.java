package ex_11052024;

public class Student {
    String name;
    int age;
    String profession;
    int experience;
    boolean isManual;

    void printName()
    {
        System.out.println("Name of the student "+name);
    }

    void printAge()
    {
        System.out.println("Age of the student "+age);
    }

    void printProfession()
    {
        System.out.println("Profession of the student "+profession);
    }

    void printExperience()
    {
        System.out.println("Experience of the student "+experience);
    }
    void printIsManual()
    {
        System.out.println("Experience on Manual is  "+isManual);
    }

}
